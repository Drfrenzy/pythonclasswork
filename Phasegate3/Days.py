print("DAY OF THE WEEK")

days = int(input("PLEASE ENTER THE BETWEEN 1 AND 7: "))

match days:
    case 1: print("MONDAY")
    case 2: print("TUESDAY")
    case 3: print("WEDNESDAY")
    case 4: print("THURSDAY")
    case 5: print("FRIDAY")
    case 6: print("SATURDAY")
    case 7: print("SUNDAY")



